package W02_Exercises_Loops.Operators;

public class Incrementations {
    public static void main(String args[]) {
        int num1 = 100;
        int num2 = 200;
        // Autoincrement
        num1++;
        // Autodecrement
        num2--;
        System.out.println("num1++ is: " + num1);
        System.out.println("num2-- is: " + num2);
    }
}
