package W02_Exercises_Loops.Operators;

public class ArithmeticOperations {
    public static void main(String args[]) {
        int num1 = 6;
        int num2 = 5;
        System.out.println("num1 + num2: " + (num1 + num2));
        System.out.println("num1 - num2: " + (num1 - num2));
        System.out.println("num1 * num2: " + (num1 * num2));
        System.out.println("num1 / num2: " + (num1 / num2));
        System.out.println("num1 % num2: " + (num1 % num2));
    }
}
